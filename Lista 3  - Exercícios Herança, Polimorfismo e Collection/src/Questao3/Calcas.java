package Questao3;

public class Calcas {
	
	private String cor;
	private int tamanho;
	
	public Calcas() {
		
	}
	
	public Calcas(String cor, int tamanho) {
		this.cor = cor;
		this.tamanho = tamanho;
	}

	public String getCor() {
		return cor;
	}

	public void setCor(String cor) {
		this.cor = cor;
	}

	public int getTamanho() {
		return tamanho;
	}

	public void setTamanho(int tamanho) {
		this.tamanho = tamanho;
	}
	
					
}
